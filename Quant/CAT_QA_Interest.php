Two equal installments annually = 2 years<br />
Appling the formula = X/(1+r/100)<sup>n</sup>…………………….X/(1+r/100)<br />
So we have here two equal installments so<br />
P= X/(1+r/100)<sup>2</sup> + X/(1+r/100)<br />
4800=X/(1+5/100)<sup>2</sup> + X/(1+5/100)<br />
On simplifying<br />
We have x= Rs. 2581.46<br />
So the amount of each installment is 2581.46</p>
<p><strong>Application of Compound Interest for concepts of population growth. </strong><br />
<br /><strong>Case 1: When population growing in a constant rate</strong><br />
If the rate growth of population increased with a constant, rate then the population after T years will be = P (1+R/100)<sup>t</sup></p>
<p>In fact, this is nothing else but an application of the fundamentals of compound interest.<br />
It is actually similar to finding the compound amount after time T years<br />
Net population after T years = = P (1+R/100)<sup>t</sup><br />
Net population increase = P [(1+R/100)<sup>t</sup>– 1]</p>
<p><strong>Example 3:</strong><br />
<strong>The population of Chandigarh is increasing at a rate of 4% per annum. The population of Chandigarh is 450000, find the population in 3 years hence.</strong><br />
Solution:<br />
P = 450000<br />
Rate of increasing = 4%<br />
Time =3 years<br />
Therefore, the total population will be<br />
=&gt; T = P (1+R/100)<sup>3</sup><br />
=&gt; T = 450000(1+4/100)<sup>3</sup><br />
=&gt; T = 506188</p>
<p><strong>Case 2: When the population growing with different rates and for different intervals of time </strong><br />
If the rate growth of population increased with different, rate and for different intervals of time then the population after T years will be =<br />
P (1+R1/100)<sup>t1 </sup>x  (1+R2/100)<sup>t2</sup> x ... x (1+RN/100)<sup>tn</sup></p>
<p>Let us take an example for this concept.<br />
<strong>Example 4:</strong><br />
<strong>The population of Chandigarh increased at a rate of 1% for first year, the rate for second year is 2%, and for third year, it is 3%. Then what will be the population after 3 years if present population of Chandigarh is 45000?</strong></p>
<p><strong>Solution:</strong><br />
Since the rate growth of population increased with different rates for the three difference years, the population after T years will be =<br />
P (1+R1/100)<sup>t1 </sup>x  (1+R2/100)<sup>t </sup>x (1+R/100)<sup>tn</sup><br />
45000(1+1/100)<sup>1</sup> x (1+2/100)<sup>1</sup> x (1+3/100)<sup>1</sup>=47749.77</p>
<p><strong>Case 3: When the population is decreasing with rate R</strong><br />
Population after a time period of T years=P (1-R/100)<sup>t</sup><br />
Where T is the total population<br />
R is rate at which the population is decreasing</p>
<p><strong>Example 5:</strong><br />
The population of Chandigarh is increases at a rate of 1% for first year, it decreases at the rate of 2% for the second year and for third year it again increases at the rate of 3%. Then what will be the population after 3 years if present population of Chandigarh is 45000.</p>
<p>Solution:<br />
Since the rate growth of population is increasing first and then decreasing for the second year and again it increases for third year, then the population after T years will be =<br />
Present Population: P (1+1/100)<sup>t1 </sup>x  (1-2/100)<sup>t </sup>x (1+3/100)<sup>t3</sup><br />
Present Population:  45000(1+1/100)<sup>1</sup> x (1-2/100)<sup>1</sup> x (1+3/100)<sup>1</sup>=45877.23</p>
<p><strong></strong><strong>Negative Compound Interest</strong><br />
It is not necessary that there is always an increase in any quantity or amount. There can also be a reduction in the amount of something. 
This rate of this reduction is called the rate of depreciation. In this case we take the interest rate to be negative.</p>
<p>Let P be the original amount.<br />
Let P1 be the new amount at the end of t years.<br />
P1 = P (1-R/100)<sup>t</sup><br />
Here R is the rate of interest (negative rate).<br />
Always remember, the rate of depreciation is nothing else but negative rate of interest.</p>
<p><strong>Example 6:</strong><br />
<strong>Manpreet bought a new car. The value of the car is Rs. 45000. If rate of depreciation is 10% per annum then what will be the value of the car after 2 years </strong></p>
