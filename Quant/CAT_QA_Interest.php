Rate of depreciation = 10%<br />
T = 2 years<br />
Therefore, the value will be after 2 years<br />
= P (1 &#8211; R/100)<sup>t</sup><br />
= 45000(1-10/100)<sup>2</sup><br />
= 36450</p>
