<p>
Having done with simple interest, it is time to be introduced to another type of interest called compound interes. 
In simple interest the interest is calculated over the principal amount and the interest keeps piling on.
However, in compound interest, you gain interest on the interest amount as well that has been piling on thus increasing returns.</p>
<p>For example, Sunil borrows a sum of Rs. 100 from Ghansham for a period of two years. The rate of interest for this loan is 10%. At the end of year one, the amount due is the principal and 10% interest on it, that is a total of Rs. 110. Now, effectively the principal value of the loan for the second year is no longer Rs. 100, it is in fact Rs. 110. That is what Ghansham would say and believe. According to him, for the 2<sup>nd</sup> year, he has lent Rs. 110 as that was the amount he would have had if he taken back the money at the end of year 1. Now for the 2<sup>nd</sup> year, the interest becomes Rs. 11 (10% of Rs. 110) and the total amount Ghansham would get would be Rs. 121.</p>
<p>If the same calculation was done using the logic of simple interest, you would see that the interest due for two years would be Rs. 20 (10% of Rs. 100 for two years). Thus, replace a S with a C and there is such a big difference in the calculations carried out.</p>
<p>Effectively, for compound interest, the 2<sup>nd</sup> term of interest is the sum total of the principal and the interest for the first term.
<p>Amount Due at the end of the time period, A =  P (1+r/100)<sup>t</sup></p>
<p>Where:<br />
<strong>P</strong>: Principal (original amount)<br />
<strong>R</strong>: Rate of Interest (in %)<br />
<strong>T</strong>: Time period (yearly, half-yearly etc.)</p>
<p>Compound Interest (CI) =  A- P = P (1+r/100)<sup>t</sup> -P<br />
= P [(1+r/100)<sup>t</sup> -1]</p>
(Questions) <br />
<p><strong>Example 1: </strong>Maninder took a loan of Rs. 10000 from Prashant . If the rate of interest is 5% per annum compounded annually, find the amount received by Prashant by the end of three years</p>
<p><strong>Solution</strong>:<br />
The following is the data given:<br />
Principal, P= 10000<br />
Rate = 5%<br />
Time =3 years</p>
<p>Using the formula for Compound Interest:<br />
<strong>A = P(1+R/100)<sup>t</sup></strong><br />
<strong>So A= </strong>10000(1+5/100)<sup>3</sup><br />
A = 10000(1+1/20)<sup>3</sup><br />
A = 10000 x 21/20 x 21/20 x21/20 =11576.25<br />
So the total amount paid by Maninder at the end of third year is Rs.11576.25</p>
<p><strong>Example 2</strong>: Richa gave Rs. 8100 to Bharat at a rate of 9% for 2 years compounded annually. Find the amount of money which she gained as a compound interest from Bharat at the end of second year.</p>
<p><strong>Solution</strong>:<br />
Principal value = 8100<br />
Rate = 9%<br />
Time = 2 years<br />
So the total amount paid by Bharat<br />
= 8100(1+9/100)<sup>2</sup><br />
=Rs.  9623.61</p>
<p>The question does not probe the amount, rather, it wants to know the CI paid, that the difference between the total amount and original principal.<br />
The Compound Interest = 9623.61 – 8100 = 1523.61</p>
<p><strong>Compound Interest Tooltip 4: Multiple Compounding in a year </strong></p>
<p>Amount Due at the end of the time period</p>
A = P (1 + r/n)<sup>nt</sup>
Where:<br />
A = future value<br />
P = principal amount (initial investment)<br />
r = annual nominal interest rate<br />
n = number of times the interest is compounded per year<br />
t = number of years money borrowed</p>
<p><strong>Amount for Half Yearly Compounding, A = P {1+(R/2)/ 100}<sup>2T</sup> </strong><br />
(compound interest applied two times an year).</p>
<p><strong>Like Half Yearly Compound Interest, we can calculate the amount for Quarterly Compounding:</strong></p>
<p><strong>A = P {1+(R/4)/ 100}<sup>4T</sup></strong></p>
<p><strong>Example 3:</strong> Sona deposited Rs. 4000 in a bank for 2 years at 5% rate. Find the amount received at the end of year by her from the bank when compounded half yearly.</p>
<p>Solution:<br />
Principal value = Rs. 4000<br />
Rate = 5%<br />
Time = 2 years<br />
Since the interest is compounded half yearly so 2 years = 4 times in two years<br />
So we have =<strong> A = P {1+(R/2)/ 100}<sup>2T</sup></strong><br />
A= 4000{1+ (5/2)/100}<sup>4</sup><br />
A = 4000 x 41/40 x 41/40 x 41/40 x 41/40<br />
A = Rs. 4415.2<br />
So, Sona received Rs. 4415.2 from the bank after two years</p>
<p><strong>Example 4:</strong> Manpreet lent Rs 5000 to Richa at 10% rate for 1 year. But she told her that she will take her money on compound interest. So find the amount of interest received by Manpreet when compounded quarterly?</p>
<p>Solution:<br />
Principal value = Rs. 5000<br />
Rate = 10%<br />
Time = 1 year<br />
Since the interest is compounded quarterly, that is 4 times in 1 year<br />
Using the formula=<strong> A = P {1+(R/4)/ 100}<sup>4T</sup></strong><br />
A= 5000{1+ (10/4)/100}<sup>4</sup><br />
A = 5000 x 41/40 x 41/40 x 41/40 x 41/40<br />
A = Rs. 5519.064<br />
So, Manpreet received Rs. 4415.2 from bank after two years<br />
And the total amount of interest received by her is 5519.064 – 5000 = Rs. 519.06</p>
<p><strong>Example 5:</strong><br />
<strong>The difference between compound interest and simple interest is 2500 for two years at 2% rate, then find the original sum.</strong></p>
<p><strong>Solution: </strong><br />
Given Interest is = 2500<br />
So, Simple Interest = (P X R X T)/100<br />
Compound Interest = P [(1+r/100)<sup>t</sup> – 1]<br />
So the difference between both of them is<br />
= PRT/100 – P [(1+R/100)<sup>T</sup> -1]<br />
= P [(1+r/100)<sup>T</sup> -1-RT/100]<br />
So the sum is 2500 = P [{(1+2/100)<sup>2</sup>-1}-4/100]<br />
On simplification this equation the sum will be = Rs. 6250000</p>
<p><strong>We can check it by our shortcut method</strong><br />
When time given is 2 years = P(R/100)<sup>2 </sup><br />
Since we are given by the difference so<br />
2500 = P (2/100)<sup>2</sup><br />
=&gt; 2500 = P (1/50)<sup>2</sup><br />
=&gt; 2500= P (1/2500)<br />
=&gt; 6250000=P</p>
<p>So the sum is Rs.6250000.</p>
<p>Similarly we can conclude the sum when time given is 3 years.</p>
<p><strong>Compound interest when compounding in a year but time is in fraction</strong></p>
<p>The formula to calculate compound interest when the time given is in fractions is as follows:<br />
A =P[(1+R/100)<sup>real part</sup>{1+(Fraction part x R/100)}]</p>
<p>Where<br />
A: Amount at the end of the time period<br />
P: Principal amount<br />
R: Rate of interest<br />
Real Part and Fraction part: For example, the time given is two and half years, then real part would be two and half years.</p>
<p><strong>Example 1: </strong><br />
<strong>Manpreet gave Rs. 1000 to Richa for 1 year 6 months. Then find the amount paid by Richa to Manpreet after this duration if rate of interest is 5% per annum compounded annually?</strong></p>
<p><strong>Solution :</strong><br />
We have a time of 1 years and 6 months = (1 year + 1/2 years)<br />
So to find the amount we will use<br />
A =P[(1+R/100)<sup>real part</sup>{1+(Fraction part x R/100)}]<br />
A = 1000[(1+5/100)<sup>1</sup>{1+{(6/12 x 5)/100]<br />
A= P[(21/20) x (41/40)]<br />
A = Rs. 1076.25
<p><strong>Equal Installments</strong></p>
Well, say, a certain sum of money is borrowed on compound interest for a certain period and it is returned with the help of equal installments.</p>
<p>Let us derive a formula where the amount is returned in two equal installments for a time period of two years.</p>
<p>Let P be the principal and r the rate of interest.</p>
<p>Step 1: P(1+r/100)=P1  (Amount for one year)<br />
Step 2: New Principal<br />
Now let X be the first installment. After giving the first installment, the principal value will change and the new principal will be = P1 <int>-</int> X<br />
P2 = P1 <int>-</int> X (1+R/100)<br />
Step 3: Amount and Interest for the second year<br />
Now the interest charged will be charged on this amount.<br />
Amount at the end of second year: [P(1+r/100)-X][1+r/100]<br />
Step 4: Since the installments are equal, this new amount has to equal X.<br />
Hence,<br />
[P(1+r/100)-X][1+r/100]=X<br />
On solving, we have<br />
P [(1+R/100)<sup>2</sup>-X (1+R/100)] = X<br />
P [(1+R/100)<sup>2</sup>] = X+X (1+R/100)<br />
Divide both sides by (1+r/100)<sup>2</sup><br />
So we left with<br />
<strong>P= X/(1+r/100)<sup>2</sup> + X/(1+r/100)</strong></p>
<p><strong>Generalizing the formula for EQUAL INSTALLMENTS </strong><br />
<strong>P= X/(1+r/100)<sup>n</sup> + X/(1+r/100)</strong><br />
Where x is the installment and n is number of installment</p>
<p><strong>Example 2: </strong><br />
<strong>Richa borrowed a sum of Rs. 4800 from Ankita as a loan . She promised Ankita that she will pay it back in two equal installments .If the rate of Interest be 5% per annum compounded annually, find the amount of each installment</strong><br />
