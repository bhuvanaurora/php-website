      <p>
Interest is defined as the "time value of money" which means that the value of money changes with time. Suppose you have Rs. 100 in the year 2010. 
In business terms if you invest the money in a bank that pays 5<int>%</int> interest, the amount would have grown to Rs. 105 in 2011
(because now the money you have is Rs. 100 that you had initially plus 5% that bank paid for depositing).</p>
<p>The sum of money borrowed or invested is called as the principal amount and the additional amount paid back over and above the borrowed the sum 
is called the interest which is in time-dependent percentage terms.</p>
<p>Simple Interest (SI) =  (P x R x T)/100</p>
<p>Where:<br />
<strong>P</strong>: Principal (original amount)<br />
<strong>R</strong>: Rate of Interest (in %)<br />
<strong>T</strong>: Time period (yearly, half-yearly etc.)</p>
<p>Amount due at the end of the time period, A = P + SI<br />
A = P + (P x R x T)/100 </p>
(Questions) <br />
(Use video for explanation of concept) <br />
<p><em>Basic Problem 1: What is the SI on Rs. 7500/- at the rate of 12% per annum for 8 years?</em><br />
Using the Basic Formula:<br />
Simple Interest (SI) =  (P x R x T)/100<br />
P – Principal amount, T- Number of years, R – Rate of Interest<br />
Given P = 7500, N = 8 Years, R = 12%<br />
S.I = (7500X12X8)/100<br />
S.I = 7200</p>
<p><em>Basic Problem 2: A man borrowed rs.15000/- at the rate of 24% SI and to clear the debt after 6 years, much he has to return:</em><br />
Using the Basic Formula:<br />
Simple Interest (SI) =  (P x R x T)/100<br />
P – Principal amount, T- Number of years, R – Rate of Interest<br />
Given P = 15000, T = 6 Years, R = 8%<br />
S.I = PNR / 100<br />
S.I =(15000X24X6)/100<br />
= 21600<br />
Therefore, total interest = 21600<br />
Total repayment = S.I + Principal amount<br />
Answer = 21600 + 15000 = Rs 36600</p>
<p><em>Basic Problem 3: A man borrowed Rs.12000 at the rate of 10% SI, and lent the same sum to another person at the rate of 15% what will be the gain after 5 years?</em><br />
Using the Basic Formula:<br />
Simple Interest (SI) =  (P x R x T)/100<br />
P – Principal amount, T- Number of years, R – Rate of Interest<br />
A man borrowed at 15%<br />
He lent the same sum to another person at 15%<br />
Therefore his gain is actually equal to the different in the interest rate (per year) 15 – 10 =5% for 1 year<br />
Thus, to calculate his gain, we use this difference as the rate of interest.<br />
Given T = 5 years and P = Rs. 12000<br />
First we are calculating for Rs. 100<br />
Amount Gained = (12000x5x5)/100 = Rs. 3000<br />
Therefore his gain = Rs. 3000/-</p>
