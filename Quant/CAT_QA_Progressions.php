                       <h2>
                            Sequences</h2>
                        <p>
                                A sequence in mathematics is defined as an ordered list of elements (usually numbers) whose order defines some underlying property of the list. The order of the elements
                                is very important and changing even one element would change the meaning of the entire sequence.</p>
                            <p>
                                The elements in a sequence are separated by commas and the length of a sequence
                                is usually denoted by the letter <b>n</b>. The length of a sequence is equal to
                                the number of terms in the sequence. Each element or term of a sequence is defined
                                by its position in the sequences for example</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/9ed97fb1-df01-4aeb-a2f7-5d1ee5a3e7f7.gif" title=""></p>
                            <p>
                                In the above, each term is denoted by <b>a</b> with a subscript number denoting
                                its position in the sequence. The first term is
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/7e5816c0-b2b2-434c-848f-a6c1eae8418b.gif" title="">
                                and the last term is
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/f200d707-5e05-4ce6-be1a-240ac77c143a.gif" title="">
                                where <b>n</b> is the length of the sequence. Sometimes the first term is also denoted
                                by
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/de3d349c-11f2-4e8c-ae29-ebdb9927acc3.gif" title="">
                                for example</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/a136de49-21e6-4aec-9400-3aa515a78561.gif" title=""></p>
                            <p>
                                The next term in a sequence is obtained by studying the relationship between the
                                preceding terms in order to obtain a relationship which we can use to obtain the
                                missing terms.</p>
                            <p>
                                For example; find the 2 next terms (denoted by <i>x</i> and <i>y</i>)in the sequence
                                below:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/06599116-d57d-474f-88e1-eee403e93fc7.gif" title=""></p>
                            <p>
                                Solution:</p>
                            <p>
                                The first step is to try and obtain the relationship between the first and second
                                term;</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/1a578267-c008-489a-876c-2bc93619d3fe.gif" title=""></p>
                            <p>
                                the difference between these two numbers is 1, 25 = 24 + 1</p>
                            <p>
                                There seems to be no other obvious relationship between these two numbers, so we
                                move on to study the second and third numbers;</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/9d4da34f-9034-4ecc-80e1-cee4f976bc2f.gif" title=""></p>
                            <p>
                                The difference here too is 1 and 26 = 25 + 1. Now you should begin to see a trend
                                in the sequence, the relationship between the terms seems to be that:
                            </p>
                            <p align="center">
                                the next term is obtained by adding one to the previous (current) term
                            </p>
                            <p>
                                Let's study one more pair to confirm that the above is true:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/afc89a97-1ccf-4037-919d-d15636c71935.gif" title=""></p>
                            <p>
                                The difference between these two numbers is 1 as we had suspected and 27 = 26 +
                                1 which proves the relationship we defined above.
                            </p>
                            <p>
                                Therefore, we can obtain <i>x</i> and <i>y</i> as follows:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/73c00563-96a7-4121-83e1-b8dddb4d5316.gif" title=""></p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/c945dce4-60f7-4ddf-9e84-bebaa3a3ccca.gif" title=""></p>
                            <p>
                                such that the sequence becomes:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/221b5ce4-7450-4ade-8ad1-8626ef68e7c7.gif" title=""></p>
                            <h2>
                                Sequence Examples</h2>
                            <p>
                                The above is a very elementary example. The relationship between the terms of a
                                sequence may not always be so easy to spot. Here's a more challenging example:</p>
                            <p>
                                Find the <i>x</i> and <i>y</i> in the sequence below</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/b64a8681-311a-4e1f-a401-c35f17b228df.gif" title=""></p>
                            <p>
                                Solution:</p>
                            <p>
                                Ok, this may not be so challenging if you have already spotted the special property
                                linking the terms in the above sequence. If you have, then let's indulge ourselves
                                and pretend that you haven't. How would you obtain <i>x</i> and <i>y</i>?</p>
                            <p>
                                The first step is to study the first two terms and try to see what kind of relationship
                                exist between them;</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/2a7978de-ee6f-49cc-a7e5-8b8b6a7a1a29.gif" title=""></p>
                            <p>
                                if we were to subtract the first term from the second term 4 - 1 = 3. For now let's
                                leave this pair and try to find out if the same relationship exists between the
                                second and third terms</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/2f147602-537d-44fd-9c0f-a794800a7636.gif" title=""></p>
                            <p>
                                Taking their difference: 9 - 4 = 5</p>
                            <p>
                                but 5 is not equal to 3. However 5 is the next odd number after 3 so let's assume
                                that the relationship between two consecutive terms is that their difference gives
                                the next odd number. This is true for the first two pairs we've seen as the next
                                odd number after 3 is 5. If the relationship is to hold for the whole sequence,
                                the difference between the terms in the next pair should be equal to the next odd
                                number, which is 7.</p>
                            <p>
                                So now let's look at the next pair to check if the above relationship does indeed
                                hold true:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/063a53ef-3dc9-4394-98be-d1e217942e11.gif" title=""></p>
                            <p>
                                taking their difference: 16 - 9 = 7</p>
                            <p>
                                The relationship is true so far but to be on the safe side, let's look at the last
                                pair</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/4520fafe-5d9f-47d2-bfb9-825b6ca5de08.gif" title=""></p>
                            <p>
                                taking their difference: 25 - 16 = 9</p>
                            <p>
                                9 is the next odd number after 7. So now we have confirmed that the relationship
                                between two consecutive terms is as follows:</p>
                            <p align="center">
                                the next term is obtained by adding the next odd number to the current term
                            </p>
                            <p>
                                From the above we can see that</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/1d775e3a-d2c3-4856-93a6-47cc1ab912e8.gif" title=""></p>
                            <p>
                                and</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/0fe3f3a3-68f8-4c10-a57b-85bbdf1ab568.gif" title=""></p>
                            <p>
                                Such that the sequence becomes</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/1a0e1f66-40cd-4945-badb-0d90778e9f86.gif" title=""></p>
                            <p>
                                We could also have obtained <i>x</i> and <i>y</i> by observing that all the terms
                                in the sequence are squares of the numbers from 1 to 5, so it would appear that
                                x would be the square of 6 which is 36 and y would be the square of 7 which is 49.</p>
                            <p> A way we can find this relationship is to plot the points on a table in terms of x and y. The x value would be the term and the y value would be the value for that term, just like a function! We can see that the relationship between the y values is obtained by adding the next odd number. Going a step further, we can see that the relationship between each odd number goes up by 2, which is always consistent. This means that the relationship of our function is quadratic! We now know the function is f(x) = x<sup>2</sup>.</p>
                            <p align="center">
                            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/a61a3369-e787-4bc2-9607-7a15873e572d.gif" style="width: 300px; height: 250px" title=""></p>
                            <p>
                                The relationship between two consecutive terms is not always a difference relationship,
                                it might be multiplication or division or something more complicated as we'll see
                                when we get to series.</p>
                            <p>
                                Here is a rather challenging example:</p>
                            <p>
                                Find the next 2 terms (<i>x</i> and <i>y</i>) in the sequence below:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/31720751-b419-40b9-90b8-44a7f717a938.gif" title=""></p>
                            <p>
                                Solution:</p>
                            <p>
                                By inspecting the above, it should be easy to see that subtracting one term from
                                the other is not going to produce some clean straight forward relationship. The
                                differences between each pair of consecutive terms are:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/c686259f-5d7b-4833-9f0a-eb8e1cb5da3a.gif" title=""></p>
                            <p>
                                There is clearly no definable relationship between those numbers above. So straight
                                away we dismiss using 'difference' as the way to come up with the relationship in
                                the sequence in question.</p>
                            <p>
                                The next options to try are multiplication and division. If we divide one term by
                                the previous term, will there be some relationship in the numbers that we obtain?</p>
                            <p>
                                If we divide the terms in consecutive pairs we end up with</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/761748b2-ed4c-4928-baa0-153d77883d42.gif" title=""></p>
                            <p>
                                which leads to:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/cdcfd421-a8f5-4516-9461-25aafaa36f49.gif" title=""></p>
                            <p>
                                looking at only 1,2,3,4,5 the relationship should be very clear. The numbers are
                                increasing in the increment of 1, so after 5 we should have 6 then 7. So we can
                                say the the next term is obtained by multiplying the current term by the position
                                of the next term.</p>
                            <p>
                                Therefore since x is the 6th term, we can obtain the value of x by multiplying 120
                                by 6
                            </p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/e11b53a8-eba0-496c-911b-fea13857b219.gif" title=""></p>
                            <p>
                                and y is obtained by multiplying x with 7</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/eac5804f-d3b3-4146-9e55-7fe2096a7712.gif" title=""></p>
                            <p>
                                The sequence thus becomes:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/b26b3b91-a61a-41b7-b9a0-94a45498b77c.gif" title=""></p>
                            <h2>
                                Series</h2>
                            <p>
                                A Series in mathematics is defined as the sum of the elements in a sequence. A series
                                is obtained by adding up all the terms in a sequence as shown in the example below</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/02ab90bb-f3aa-4283-93f9-c117c6a96973.gif" title=""></p>
                            <p>
                                A series is denoted by the letter <b>S</b> and the length is also denoted by the
                                letter <b>n</b> as in:</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/6cdbfbb0-5083-42c4-b156-634ee5497c6b.gif" title=""></p>
                            <p>
                                for example</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/37482876-ef2d-4497-8786-bb81223f2510.gif" title=""></p>
                            <p>
                                Since a series is a sum, we can also represent it mathematically as</p>
                            <p align="center">
                                <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/52885616-aa6f-416d-9eb4-23c4d58c87ac.gif" title=""></p>
                            <p>
                                where <i>i</i> represents the position of the term <b>a</b>. The above means that
                                a series is the sum of all terms from <b>a</b><sub>1</sub> to <b>a</b><sub>n</sub>
                                where <b>n</b> is the length of the sequence.</p>
                            <p>
                                Since a series is a sum of a sequence, the terms in a series also have a special
                                relationship that defines some underlying property of the series and can be used
                                to find the next term in the series is the sum of a given number of terms in the
                                series. Most series that we deal with are of a finite nature, they don't go on forever
                                like some sequences do. We'll soon see why it is important to have series that terminate.</p>
                            <p>
                                In the section on Sequences above, we established the relationship between consecutive
                                terms of a sequence and we have already said that the same applies to the terms
                                of a series. So knowing this, we can find any number of terms in a series and find
                                its sum.
                            </p>        
