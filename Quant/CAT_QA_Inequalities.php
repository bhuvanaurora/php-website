        <p>
            Solving inequalities is not so different from solving regular equations. In fact,
            an inequality sign <b>(&lt;,&gt;,&le;,&ge;)</b> is treated the same as an equal
            (=) sign when solving inequalities involving only addition or subtraction. Before
            all that, let us define the different inequality signs:</p>
        <ul class="StyledUL">
            <li><b>&lt;</b> indicates that the expression on the left is <b>less than</b> the expression
                on the right, for example:
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/d3fc639f-5156-436d-a77b-f1fba3f2d2a0.gif" title="">
                </p>
                <p>
                    indicates that 3 is less than 9 and we all know that to be true.</p>
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/2f5de463-ebf7-4682-a4e9-b1dff24676b4.gif" title="">
                </p>
                <p>
                    means that the solution to the expression on the left is less than zero, but more
                    on that later.</p>
            </li>
            <li><b>&gt;</b> indicates that the expression on the left is <b>greater than</b> the
                expression on the right, for example:
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/1b1f4117-52da-41c1-945b-c8d01cde6c42.gif" title="">
                </p>
                <p>
                    shows that 9 is greater than 3 and similarly</p>
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/ccbdf0ca-752f-44cd-a4d2-2b1d8549bfc0.gif" title="">
                </p>
                <p>
                    means that the value of<i> 2x - 9</i> is greater than 11</p>
            </li>
            <li><b>&le;</b> indicates that the value on the left is <b>less than or equal to</b>
                the value on the right, for example:
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/c5721766-706a-4a9a-9644-38d2593d2ce8.gif" title="">
                </p>
                <p>
                    means that the value of the expression on the left must be less than or equal to
                    25</p>
            </li>
            <li><b>&ge;</b> indicates that the value on the left is <b>greater than or equal to</b>
                the value on the right, for example:
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/49d41eab-ae59-47a6-bc1d-5f5f747c0a43.gif" title="">
                </p>
            </li>
        </ul>
        <p>
            Inequalities are best thought of as representing different regions on a number line:</p>
        <ul class="StyledUL">
            <li><b>&lt;</b> represents the region to the left of a given number, for example
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/d5bc7906-4dcd-4e8f-9f43-e181bb93a3ed.gif" title="">
                </p>
                <p>
                    represents all the numbers to the left of 3 (less than 3), which in on the number
                    line below is shown by all the numbers in the direction of the arrow</p>
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/e523f655-7068-42f2-b9f2-84ba1050a115.gif" title="">
                </p>
            </li>
            <li><b>&gt;</b> represents the region to the right of a given number, for example
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/b91ebda8-7c21-4f0a-bf3a-867ded4ed2da.gif" title="">
                </p>
                <p>
                    represents all the numbers to the right of 3 (greater than 3), which in on the number
                    line below is shown by all the numbers in the direction of the arrow</p>
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/f03f109e-a035-44da-bfb3-4f9933d82810.gif" title=""></p>
            </li>
            <li><b>&le; </b>represents the region from a given number to the left of that same number,
                for example
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/79f2e88a-2b39-4075-b712-94fa714d4fad.gif" title="">
                </p>
                <p>
                    represents all the numbers to the left of 5 (less than 5) including 5 itself as
                    shown on the number line below</p>
                <p align="center">
                    <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/e0583d00-32b3-4328-8d05-f96c323b71de.gif" title=""></p>
            </li>
            <p>
                <li><b>&ge; </b>represents the region from a given number to the right of that same
                    number, for example;
                    <p align="center">
                        <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/54826127-5f2a-45ee-bbe6-def33547a60f.gif" title="">
                    </p>
                    <p>
                        represents all the numbers to the left of 1 (less than 1) including 1 itself as
