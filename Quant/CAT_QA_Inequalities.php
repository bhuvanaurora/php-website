        </p>
        <p>
            and the answer would be</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/50557927-73cd-4b06-82e5-159e329bc6f5.gif" title="">
        </p>
        <p>
            Observe that the above is solved in the same was as solving ordinary equations.
            This is because addition and subtraction have no effect on the inequality sign.
            Multiplication and division are different, however, as the inequality sign is treated
            differently depending on whether you are multiplying by a positive or negative number.
        </p>
