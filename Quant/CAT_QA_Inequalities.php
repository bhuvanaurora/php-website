        <h2>
            Solving Inequalities</h2>
        <p>
            Most linear inequalities can be solved just the same as linear equations: Addition
            and subtraction of any number (positive or negative) can be done to the expression
            on either side of the inequality without changing the inequality itself. In other
            words, it would be the same as in any ordinary equation.
        </p>
        <p>
            <b>For example</b>; solve for x in</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/e7c73f07-725f-44a7-b8eb-762726e1275f.gif" title="">
        </p>
        <p>
            <b>Solution</b>:
        </p>
        <p>
            Subtract 9 from both sides of the inequality as follows:</p>
        <p align="center">
