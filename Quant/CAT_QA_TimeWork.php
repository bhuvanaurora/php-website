       <p>
Just as in time, speed and distance, speed is the rate of covering the distance, similarly speed of doing work is the rate of getting 
work done. So, (Time for the work to be done) = (Amount of work)/(Rate of doing work).<br />
Example: If Raju and Kaju can do a job in 10 days and 15 days independently. How many days would they take to complete the job working 
together?<br /><br />
If the total work is, say W, then Raju's rate of working is W/10 and that of Kaju is W/15. So, as
(Time for the work to be done) = (Amount of work)/(Rate of doing work) = (W)/(W/10 + W/15) = 6 days.
<br />Alternately, W can be assumed to be 1 unit to reduce the caluculations.<br />
