        <p>
            A set is one of the most fundamental concepts in mathematics. Sets can be taught
            at an elementary level all the way through higher level mathematics.</p>
        <p>
            A <b>set</b> is defined as a group or collection of distinct objects. The elements
            of a set can be anything: numbers, people, letters, etc. The way we usual denote
            sets is by giving them capital letters for a name.</p>
        <p>
            Given set A and B</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/8020280c-a588-4195-8c34-d58c8760b654.gif" style="width: 300px; height: 240px" title=""></p>
        <p>
            A is equal to B <i>if and only if</i> they have precisely the same elements.</p>
        <h2>
            Notation</h2>
        <p>
            There are different ways of notating and describing sets and more specifically,
            members of a set. One way is <b>intensional definition</b>. We can literally describe
            what the set contains.</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/9a38e102-53ed-4e68-ad20-0760b7671ebb.gif" title=""></p>
        <p>
            Another way is by <b>extension</b>, which means listing each member of the set and
            enclosing them with brackets. This is also called <i>strict enumeration</i>.</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/074fd800-2811-41ac-bfe5-d1df9ce51a62.gif" title=""></p>
        <p>
            Each element of a set must be unique, and the order of the elements in the set does
            not matter.</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/d2f3143b-098b-4542-960b-0c180b029975.gif" title=""></p>
        <p>
            When we have sets with many elements, we can abbreviate the list to denote an extension
            of numbers in the same pattern as the numbers before it. This is sometimes called
            <i>pattern enumeration</i>. If we were to list all of the odd numbers up to 100,
            we would have</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/48e1c2a8-402f-4232-a1d2-8629df571e43.gif" title=""></p>
        <p>
            If it was an infinite list, we can use it at the end. Here is the list of all positive
            odd numbers</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/915725cd-5a5a-4154-850d-5cd3bb66df34.gif" title=""></p>
        <p>
            Set's can also be defined by a rule. The set A of the positive integers that are
            perfect squares less than 10 can be notated as</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/60193d4b-e748-4da8-8b51-7a1aaea7bd1d.gif" title=""></p>
        <p>
            Set names are subjective, and different definitions can be the same set of numbers.
            It is up to the mathematician to accurately describe the set.</p>
        <h2>
            Subsets</h2>
        <p>
            Subsets introduce the notion of membership of sets - when one set is contained in
            another. If every member of A is also a member of set B, then A is a subset of B,
            which is written as
        </p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/7c70357a-d3c8-4068-a22d-842c33d4ed26.gif" title=""></p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/70f35df2-1821-40f7-bdfc-233a1a75d1b1.gif" style="width: 300px; height: 240px" title=""></p>
        <p>
            The relationship is called <i>inclusion</i> or <i>containment</i>. The set <b>{1,2,4}</b>
            would be a subset of <b>{1,2,3,4}</b>. The empty set is also a subset of every set
            and every set is a subset of itself.</p>
        <p>
            When <b>A is a subset of B</b> and <b>B is a subset of A</b>, then <b>A = B</b>.</p>
        <p align="center">
            <img alt="" src="http://dj1hlxw0wr920.cloudfront.net/userfiles/wyzfiles/b342e834-c888-4aa3-a619-129e9c135b03.gif" title=""></p>
