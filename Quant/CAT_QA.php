                                    <li> Inequalities</li>
                                    <li> Equations </li>
                                    <li> Functions </li> 
                                    <li> Progressions </li>
                                    <li> Logarithms & Exponents </li>
                                </ul> </br>

                            <li> <strong> NUMBER SYSTEMS </strong>  </li>
                                <p> Number System problems are usually very simple but at times they become quite typical if approached wrongly. They require certain tricks that you can pick up from any good textbook. Number Base System also deserves the mention here. This contributes 3-4 questions to every CAT, and so it is a very important topic. You should be comfortable writing numbers in their algebraic form. You should also learn about divisibility tests and the other points that will the handling of numbers pretty easy. </p>
                                <ul>
                                    <li> Numbers </li>
                                    <li> Factors and Multiples </li> 
                                    <li> HCF-LCM </li>
                                    <li> Divisibiity </li>
                                    <li> Remainders </li>
                                    <li> Factorials</li>
                                </ul> </br>

                            <li> <strong> GEOMETRY </strong> </li>
                                <p> These are diagram based hence they are combined together. Maximum weightage is given to geometry among the three of them, although every CAT paper will have 3-4 questions on mensuration, as well as a couple of questions on coordinate geometry. Topics that need to be covered in geometry are basic theorems involving triangles, circles, parallel lines and other shapes. Similarity and Congruency of triangles is an important property to look after for as they shape the pattern and type of the questions. </p>
                                <ul>
