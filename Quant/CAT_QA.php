                <h3 id="section2"> SYLLABUS</h3>
                    <p> The Quant syllabus for CAT can be categorised into 5 main categories:-</p>
                        <ol>
                            <li> <strong> ALGEBRA </strong></li>
                                <p> Algebra itself comprises of multiple sections which are equally important and a serious aspirant should get his hand into practising all of them. </p>
