<p>
Speed is the rate at which distance is covered and hence the correlation Speed = Distance/Time or distance travelled per unit time.
<br />1 km/hr = 1000m/3600s = 1000/3600 m/s = 5/18 m/s<br /><br />
Speed is directly proportional to distance, given time is constant. Which means, say, if a car travels at twice the speed of a truck
it'll cover double the distance as covered by the truck in the same time.<br />
Similarly, speed is inversely proportional to time, given the distance is constant. The car in the above example, 
travelling at double the speed of 
the truck will cover the distance between Mumbai and Pune in half the time.<br />
Also, distance and time are directly proportional, given the speed is constant.<br />
<br />

<strong>Relative speed</strong><br />
Relative speed is the phenomenon which is observed when the observer is herself/hemself in motion.
For example, while travelling in a car at 100 kmph, the car passing by seems to be travelling at a very low speed.
In reality, the other car is travelling at over 100 kmph to pass by your car.
<br />Similarly a car travelling in the opposite direction seems to be travelling at much higher speed than it actually is.
<br /><br />(Video explaining the concept of relative speed) <br />
<br />Example: Consider two friends Tango and Charlie, 500mts apart, are runnning towards each other at a speed of 
2.5 m/s. After how long will the friends meet?<br />
Let's break down the problem and see what happens every second. After 1st second both Tango and Charlie would have travelled 
2.5 m which means they covered a distance of 5 m in all. At the end of the 2nd second they would have travelled another 
2.5 m each. In total, they would have travelled 10 m by the end of 2nd second. As we observe here, they are covering the 
distance between them at a speed of 5 m/s. To cover 500 mts it'll take them 500/5 = 100 secs.
<br />In the above example had the two been moving at speeds of 2 m/s and 3 m/s respectively, then it'd have taken the same 
amount of time, because on breaking down the problem both are covering the distance at 5 m/s.
<br />In terms of relative speed, if we assume on of them to be at rest then the other must be travelling at 5 m/s (2 m/s + 3 m/s) 
to cover the entire distance in the same amount of time.
<br /><br />
Consider another case with same guys, but this time Tango running at 3 m/s is chasing Charlie who is running at 2 m/s. The distance 
between them initially is 500 mts.<br />
After 1st second had Tango been stationary the distance would have increased to 500 + 2 = 502 mts. But, with Tango running 
at 3 m/s the distance changed to 500 + 2 - 3 = 499 mts. Similarly, at the end of 2nd second the distance would change to 
499 + 2 - 3 = 498 mts. Since the the distance is being covered at 1 m/s, Tango would have taken 500/1 = 500 sec to chase down 
Charlie. In terms of relative speed, if we consider Charlie to be at rest, then Tango must be travelling at 3 - 2 = 1 m/s.
