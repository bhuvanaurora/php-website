    <p>
        A logarithm of a number to a given base is defined as the power to which the base
        is raised in order to produce that number. In simpler terms, a logarithm is the
        opposite of an <a title="Exponents Help" href="/resources/lessons/math/algebra/exponents">exponent</a>:
        A logarithm is the operation you perform to undo an exponent. Just as division on
        a number would undo whatever multiplication was done to the original number, logarithms
        and exponents have a similar relationship.</p>
