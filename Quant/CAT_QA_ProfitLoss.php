<p>Whenever we see a transaction the keywords in play are profit and loss. Some other commonly used keywords are described under.</p>
<p><strong>Cost Price: </strong>The price paid to purchase a product or the cost incurred in manufacturing a product is known as the cost price (CP) of that product.
This price is simply the cost to the company or the individual</p>
<p><strong>Types of cost </strong><br />
1. <strong>Fixed cost</strong> it is a type of cost which is fixed under all conditions and does not vary according to the number of units produced.</p>
<p>2. <strong>Variable cost</strong>: Variable cost is a type of cost which varies according to the number of units. This is pretty understandable.
<p>3.<strong> Semi-variable cost</strong>: As the name suggests, these costs are the ones that are fixed in part and variable in part. Effectively, this is the case that we see most often.</p>
<p>Imagine the scenario in a factory. There is a capital cost, which remains the same under all conditions (fixed cost) and a variable cost of the product, which in turn depends upon various factors.</p>
<p><strong>Selling Price: </strong>The price at which a product is sold is called the selling price (SP) of the product. <strong></strong><br />
<strong>Marked Price:  </strong>The price at which the product is offered or the price mentioned on the price tag is the marked price. 
The seller may offer a discount on this price thus making it different from the selling price.</p>
<p><strong>List Price: </strong>List price or the tag price is the price that is printed on the tag of the article. For all practical purposes, we assume it to be same as the marked-price. <strong></strong></p>
<p><strong>Margin: </strong>The difference between the selling price and cost price is called margin. 
Generally the term margin is used to refer profit on the article.<strong></strong></p>
<p><em>Basic Problem 1: </em><em>Pranav sell an article at a discount of 80% and get a profit of 60% on that article calculate the mark up over the cost price? </em></p>
<p>Let us put the above definitions in use.<br />
Let us assume that Cost Price = Rs 100.<br />
So, Selling Price = Rs 160.<br />
Now, after giving a discount of 80% over MP, Rs 160 is the SP.<br />
Let the Marked Price: MP<br />
SP = 20% of MP<br />
160 = 20% of MP<br />
MP = 800<br />
% Mark Up = (700/100) * 100 = 700 %.</p>
<p><strong><em>Things to remember in this question:</em></strong><br />
<strong><em>Mark Up over Cost Price = Marked Price – Cost Price = M</em></strong><br />
<strong><em>% Mark Up Cost Price = (M/CP) * 100</em></strong></p>
<p><strong><em>Mark Up over Selling Price = Marked Price – Cost Price = M</em></strong><br />
<strong><em>% Mark Up Selling Price = (M/SP) * 100</em></strong></p>
<p><strong><em>Focus closely on what is being asked. Remember Profit and Loss is nothing else but an application of Percentages and basic calculation skills.  </em></strong></p>
<p>Profit is made on a transaction if Selling Price&gt; Cost Price<br />
Loss is made on a transaction when Selling Price &lt; Cost Price.<br />
Profit = Selling Price <int>-</int> Cost Price<br />
% Profit = 100 * (Selling Price - Cost Price) / Cost Price<br />
Loss = Cost Price &#8211; Selling Price<br />
% Loss = 100 * (Cost Price &#8211; Selling Price) / Cost Price</p>
<p><strong>Profit and Loss Tooltip 3: Multiplying equivalents to figure out the sale price</strong><br />
If there is a profit of P %,<br />
Cost Price = C<br />
Then<br />
SP =  {C*(100+P)} / 100</p>
<p>If there is a loss of L %,<br />
Cost Price = C<br />
Then<br />
SP = {C*(100-L)} / 100</p>
<p><em>Basic Problem 2: </em><em>Mehak and Pranav sells some article for Rs 8000 each. Mehak calculates her profit per cent on his CP and Pranav calculates his profit per cent wrongly on SP. What is the difference in their actual profit if both claim to have a profit of 60%? </em></p>
<p><strong>For Mehak</strong><br />
SP = Rs 8,000<br />
Profit = 60% of CP<br />
CP = Rs 5000<br />
Profit = Rs 3000<br />
<strong>For Pranav</strong><br />
SP = Rs 8,000<br />
Profit = 60% of SP = 60% of 8000 = 4800<br />
CP = Rs 3,200<br />
Profit = Rs 4800<br />
So, the difference in profit = Rs 1800</p>
<p><em>Basic Problem 3: </em><strong><em> </em></strong><em>Megha and Richa sold two articles at Rs 12,000 each.  One is sold at a profit of 20% and another one at a loss of 20%. What is the net loss? </em><br />
For article-1<br />
SP 1 = Rs 12,000<br />
CP 1 = Rs 12,000/1.2 = Rs 10,000<br />
For article-2<br />
SP 2 = Rs 12,000<br />
CP 2 = Rs 12,000/0.8 = Rs 15,000<br />
So, total CP = Rs 25,000 and total SP = Rs 24,000<br />
So, loss = Rs 1,000.</p>
