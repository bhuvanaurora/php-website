<p><strong>Prime Numbers and Composite numbers </strong><br />
Natural numbers can further be classified into prime numbers and composite numbers<br />
</p>
<p><strong> Prime numbers </strong><br />
The numbers which are completely divisible only by 1 and the number itself are called as prime numbers.<br />
For example  2, 3, 5, 7, 9, 29, 31, 43&#8230;&#8230;<br />
There are 24 prime numbers between 1 and 100.</p>
<p><strong>Properties of Prime Numbers </strong></p>
<ul>
<li>If <em>p</em> is a prime number and <i>p</i> divides a product <em>a.n </em>where both <i>a</i> and <i>n</i> are integers, then <em>p</em> divides <em>a</em> or <em>p</em> divides <em>n</em> or both.<strong></strong></li>
<li>Number of prime numbers is infinite. However, the largest known prime number is 2<sup>57,885,161</sup>&#160;−&#160;1 with 17,425,170 digits</li>
<li>Every Prime number have exactly two factors or divisors 1 and the number itself. Example: 13 is a prime number, and divisors of 13 are 1 and 13</li>
<li>Only one even prime number exists which is 2</li>
<li>The divisibility of a prime number can be checked by checking the number's divisibility for all the prime numbers from 2 to &radic;number.</li>
<li>True decimal numbers are neither prime nor composite. So, a number like 3.4 is neither prime nor composite, whereas a number like 3.0 is a prime number because 3.0 is not a true decimal number.</li>
</ul>
<p>Co-prime numbers are numbers that do not have any common factors except 1. <a href="lcm&hcf.php">HCF</a> of co-prime numbers is always 1, example: 3 and 8 are co-primes so, <a href="lcm&hcf.php">HCF</a> of 3 and 8 is 1</p>
<ul>
<li>All prime numbers greater than 3 are of the form (6k - 1) or (6k + 1) where k is a natural number. The vice-versa however, is not true. Example: 5, 7, 13, 17, 19, 31, 61, 89, 107 etc.</li>
<li>The Twin Primes Conjecture: There are infinitely many pairs of primes only 2 apart.</li>
<li>First Prime Number is 2. The number 1 is neither prime nor composite.</li>
</ul>
      
      
     <p><strong>TO CHECK WHETHER A NUMBER IS PRIME OR NOT </strong></p>
<p>To check whether a given number is prime or not, first find the square root of that number and then approximate that to immediately lower integer (say n) and write down all the prime numbers less than that integer. Then check the divisibility of the given number by all the prime numbers, if it is not divisible by any prime numbers then given number is prime.</p>
<p>To find whether given number N is a prime or not.</p>
<p><strong>Step 1</strong>: Find squre root of N say it as K (Just find approximate value)</p>
<p>Let N = 211, approximate square root is 14.xxx. (Means more than 14 but less than 15)</p>
<p><strong>Step 2</strong>: Write down all the prime numbers less than K  ,All the prime numbers less than 14 are 2, 3, 5, 7,11, and 13.</p>
<p><strong>Step 3</strong>: Check divisibility of N with these prime numbers. Check divisibility of 211 by 2, 3, 5, 7, 11 and 13, you will find that it is not divisible by any of these  prime numbers, hence 211 is a prime number.</p>
