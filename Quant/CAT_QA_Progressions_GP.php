        <p>
            A progression is another way of saying sequence thus a Geometric Progression is
            also known as a Geometric Sequence.</p>
        <p>
            A Geometric Progression is a special sequence defined by the special property that
            the ratio of two consecutive terms is the same for all the terms in the sequence.
            Whereas in Arithmetic Progression we talked of difference, here we talk of ratios
            meaning that when you divide the current term by the previous term the number that
            you get should be a non zero constant that is the same for all the consecutive pairs
            of terms in the sequence. This number is known as the common ratio and is denoted
            by the letter <b>r</b>
        </p>
